// 7. Зацикліть калькулятор. Запросіть у користувача 2 числа і знак, розв’яжіть приклад, виведіть результат і запитайте, чи хоче він розв’язати ще один приклад. І так триватиме доти, доки користувач не відмовиться.
let repeat;
do {
  let num1 = +prompt("Введіть перше число:");
  let operator = prompt("Введіть операцію (+, -, *, /):");
  let num2 = +prompt("Введіть друге число:");
  let result;
  switch (operator) {
    case "+":
      result = num1 + num2;
      break;
    case "-":
      result = num1 - num2;
      break;
    case "*":
      result = num1 * num2;
      break;
    case "/":
      result = num2 !== 0 ? num1 / num2 : "Ділення на нуль!";
      break;
    default:
      result = "Невідома операція";
  }
  alert(`Результат: ${result}`);
  repeat = confirm("Хочете розв'язати ще один приклад?");
} while (repeat);
// 8. Виведіть таблицю множення для всіх чисел від 2 до 9. Кожне число необхідно помножити на числа від 1 до 10.
let table = "";
for (let i = 2; i <= 9; i++) {
  for (let j = 1; j <= 10; j++) {
    table += `${i} × ${j} = ${i * j}\n`;
  }
  table += "\n";
}
alert(table);
// 9. Підрахуйте суму всіх чисел у заданому користувачем діапазоні.
let start = +prompt("Введіть початок діапазону:");
let end = +prompt("Введіть кінець діапазону:");
let sum = 0;
for (let i = Math.min(start, end); i <= Math.max(start, end); i++) {
  sum += i;
}
alert(`Сума чисел у діапазоні: ${sum}`);
