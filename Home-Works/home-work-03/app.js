// 5. Напишіть конвертор валют. Користувач вводить кількість USD, вибирає в яку валюту хоче перекласти: EUR, UAN або AZN, і отримує відповідну суму.
let usdAmount = +prompt("Введіть суму в USD:");
let currency = prompt("У яку валюту перевести? (EUR, UAN, AZN)").toUpperCase();
let rate;
switch (currency) {
  case "EUR":
    rate = 0.93;
    break;
  case "UAN":
    rate = 39.2;
    break;
  case "AZN":
    rate = 1.7;
    break;
  default:
    alert("Невідома валюта.");
    rate = 0;
}
if (rate) {
  alert(`Сума у ${currency}: ${(usdAmount * rate).toFixed(2)}`);
}
